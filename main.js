let currentLang = 'pt';

function updateContent() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    const keys = key.split('.');
    let translation = translations[currentLang];
    
    keys.forEach(k => {
      translation = translation[k];
    });
    
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translation;
    } else {
        el.innerHTML = translation;
    }
  });

  document.getElementById('lang-toggle').textContent = currentLang === 'pt' ? 'EN' : 'PT';
  document.documentElement.lang = currentLang === 'pt' ? 'pt-BR' : 'en';
}

window.openModal = (projectId) => {
    const modal = document.getElementById('case-modal');
    const stackList = document.getElementById('stack-list');
    const data = projectData[projectId];
    const caseTrans = translations[currentLang].cases[projectId];
    const headers = translations[currentLang].case_headers;
    
    if (caseTrans) {
        // Update Modal Content using IDs
        document.getElementById('modal-tag').textContent = caseTrans.tag;
        document.getElementById('modal-title').textContent = caseTrans.title;
        document.getElementById('modal-vision-title').textContent = headers.vision;
        document.getElementById('modal-vision-text').textContent = caseTrans.vision;
        document.getElementById('modal-challenge-title').textContent = headers.challenge;
        document.getElementById('modal-challenge-text').textContent = caseTrans.challenge;
        document.getElementById('modal-solution-title').textContent = headers.solution;
        document.getElementById('modal-solution-text').textContent = caseTrans.solution;
        document.getElementById('modal-results-title').textContent = headers.results;
        document.getElementById('modal-results-text').textContent = caseTrans.results;
        document.getElementById('modal-stack-title').textContent = headers.stack;
        
        document.getElementById('modal-img').src = data.image;
        stackList.innerHTML = data.stack.map(s => `<li><strong>${s}</strong></li>`).join('');
    }
    
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
};

window.closeModal = () => {
    const modal = document.getElementById('case-modal');
    modal.classList.remove('active');
    document.body.style.overflow = 'auto';
};

document.getElementById('lang-toggle').addEventListener('click', () => {
  currentLang = currentLang === 'pt' ? 'en' : 'pt';
  updateContent();
});

// Scroll Effects
window.addEventListener('scroll', () => {
  const header = document.getElementById('main-header');
  const scrollProgress = document.getElementById('scroll-progress');
  
  // Progress Bar
  const winScroll = document.body.scrollTop || document.documentElement.scrollTop;
  const height = document.documentElement.scrollHeight - document.documentElement.clientHeight;
  const scrolled = (winScroll / height) * 100;
  scrollProgress.style.width = scrolled + "%";
  
  // Header shadow
  if (winScroll > 50) {
    header.classList.add('scrolled');
  } else {
    header.classList.remove('scrolled');
  }
});

// Observer for reveals
const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('active');
        }
    });
}, { threshold: 0.1 });

window.addEventListener('load', () => {
    updateContent();
    
    document.querySelectorAll('.animate-on-scroll').forEach(el => {
        revealObserver.observe(el);
    });
});

// Form Submission with Formspree AJAX
document.getElementById('contact-form').addEventListener('submit', async function(e) {
    e.preventDefault();
    const form = e.target;
    const btn = form.querySelector('.btn-primary');
    const originalText = btn.textContent;
    
    // Change button state
    btn.disabled = true;
    btn.textContent = currentLang === 'pt' ? 'Enviando...' : 'Sending...';
    
    try {
        const response = await fetch(form.action, {
            method: form.method,
            body: new FormData(form),
            headers: {
                'Accept': 'application/json'
            }
        });
        
        if (response.ok) {
            alert(currentLang === 'pt' ? 'Consultoria solicitada! Entraremos em contato em breve.' : 'Consultation requested! We will be in touch soon.');
            form.reset();
        } else {
            const data = await response.json();
            if (Object.hasOwn(data, 'errors')) {
                alert(data["errors"].map(error => error["message"]).join(", "));
            } else {
                alert(currentLang === 'pt' ? 'Ops! Houve um erro no envio.' : 'Oops! There was an error sending your message.');
            }
        }
    } catch (error) {
        alert(currentLang === 'pt' ? 'Erro de conexão. Tente novamente.' : 'Connection error. Please try again.');
    } finally {
        btn.disabled = false;
        btn.textContent = originalText;
    }
});
